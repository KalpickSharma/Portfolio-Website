import gsap from "gsap";

function createCharSpans(selector: string) {
  const elements = document.querySelectorAll(selector);
  elements.forEach(element => {
    const text = element.textContent || '';
    element.textContent = '';
    text.split('').forEach(char => {
      const span = document.createElement('span');
      span.textContent = char;
      span.style.display = 'inline-block';
      element.appendChild(span);
    });
  });
  return Array.from(document.querySelectorAll(`${selector} span`));
}

export function initialFX() {
  document.body.style.overflowY = "auto";
  document.getElementsByTagName("main")[0].classList.add("main-active");
  gsap.to("body", {
    backgroundColor: "#0b080c",
    duration: 0.5,
    delay: 1,
  });

  // Animate landing text
  const landingChars = createCharSpans(".landing-info h3, .landing-intro h2, .landing-intro h1");
  gsap.fromTo(
    landingChars,
    { opacity: 0, y: 80, filter: "blur(5px)" },
    {
      opacity: 1,
      duration: 1.2,
      filter: "blur(0px)",
      ease: "power3.inOut",
      y: 0,
      stagger: 0.025,
      delay: 0.3,
    }
  );

  // Set up text alternating animation
  const timeline = gsap.timeline({ repeat: -1 });
  
  // Designer/Developer text animation
  timeline
    .to(".landing-h2-2", { opacity: 1, duration: 0.5, delay: 2 })
    .to(".landing-h2-1", { opacity: 0, duration: 0.5 }, "<")
    .to(".landing-h2-2", { opacity: 0, duration: 0.5, delay: 2 })
    .to(".landing-h2-1", { opacity: 1, duration: 0.5 }, "<");

  // Secondary Designer/Developer text animation
  timeline
    .to(".landing-h2-info-1", { opacity: 1, duration: 0.5 }, "<")
    .to(".landing-h2-info", { opacity: 0, duration: 0.5 }, "<")
    .to(".landing-h2-info-1", { opacity: 0, duration: 0.5, delay: 2 })
    .to(".landing-h2-info", { opacity: 1, duration: 0.5 }, "<");

  // Animate nav links
  gsap.fromTo(
    ".nav-links a",
    { opacity: 0, y: -50 },
    {
      opacity: 1,
      duration: 0.8,
      ease: "power2.inOut",
      y: 0,
      stagger: 0.1,
      delay: 0.5,
    }
  );

  gsap.fromTo(
    ".landing-info-h2",
    { opacity: 0, y: 30 },
    {
      opacity: 1,
      duration: 1.2,
      ease: "power1.inOut",
      y: 0,
      delay: 0.8,
    }
  );
  gsap.fromTo(
    [".header", ".icons-section", ".nav-fade"],
    { opacity: 0 },
    {
      opacity: 1,
      duration: 1.2,
      ease: "power1.inOut",
      delay: 0.1,
    }
  );
}
